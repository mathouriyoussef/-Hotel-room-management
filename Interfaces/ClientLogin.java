    public boolean formEmail(){
       return Pattern.matches("^[A-Za-z0-9+_.-]+@(.+)$", txtEmail.getText());
    }
    private boolean formValide(){
        return !(txtEmail.getText().trim().equals("") || 
                txtPassword.getText().trim().equals(""));
    }
    private void cmdLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLoginActionPerformed
        if(!formValide()){
            JOptionPane.showMessageDialog(this, "Form invalide.");
            return;
        }     
        if(!formEmail()){
            JOptionPane.showMessageDialog(null, "Veuillez saisir un e-mail valide", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Client c = cc.getByEmail(txtEmail.getText(),txtPassword.getText());
        if(c == null){
            JOptionPane.showMessageDialog(null, "Email or Password is incorrect.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(this, "Connexion r√©ussie.");
        if(!cc.isAdmin(c.getGroupId())){
            ClientReservation cr = new ClientReservation(c);
            cr.setVisible(true);
            this.setVisible(false);
        }else{
            GerantReservation gr = new GerantReservation();
            gr.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_cmdLoginActionPerformed

