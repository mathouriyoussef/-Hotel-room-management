    private void cmdAccepterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAccepterActionPerformed
       
        int SelectedRowIndex = tableReservations.getSelectedRow();
        if(SelectedRowIndex == -1){
            JOptionPane.showMessageDialog(null, "Aucun reservation selectionner!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int resId = (int) tableReservations.getValueAt(SelectedRowIndex, 0);
          
        if(rc.getStatus(resId, 2)){
            JOptionPane.showMessageDialog(this, "La réservation est déjà acceptée");
            return ;
        }
        if(rc.getStatus(resId, 3)){
            JOptionPane.showMessageDialog(this, "La réservation est déjà refusée");
            return ;
        }
        
        if(rc.Accept(resId) > 0){
            JOptionPane.showMessageDialog(this, "La réservation acceptée");
            listerReservations(c);
               try {
            
            rc.sendAccept(resId);
        } catch (SQLException ex) {
            System.out.println("errur acceptte  email");
        } 
        }else{
            JOptionPane.showMessageDialog(null, "Echec!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
          
        
    }//GEN-LAST:event_cmdAccepterActionPerformed

    private void cmdRefuserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdRefuserActionPerformed

        int SelectedRowIndex = tableReservations.getSelectedRow();
        if(SelectedRowIndex == -1){
            JOptionPane.showMessageDialog(null, "Aucun reservation selectionner!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int resId = (int) tableReservations.getValueAt(SelectedRowIndex, 0);
        if(rc.getStatus(resId, 3)){
            JOptionPane.showMessageDialog(this, "La réservation est déjà refusée");
            return ;
        }
        // En Attente = 1
        // Accepter = 2
        // Refuser = 3
        if(rc.Refuse(resId) > 0){
            JOptionPane.showMessageDialog(this, "La réservation refusé");
            listerReservations(c);
             try {
            
            rc.sendRefuse(resId);
        } catch (SQLException ex) {
            Logger.getLogger(GerantReservation.class.getName()).log(Level.SEVERE, null, ex);
        } 
        }else{
            JOptionPane.showMessageDialog(null, "Echec!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_cmdRefuserActionPerformed

