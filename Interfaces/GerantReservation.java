    private void listerClients(){
        DefaultTableModel m = new DefaultTableModel();
        m.setColumnIdentifiers( new String[]{"ID","Name","Email","Group"});
        for(Client c : cc.getAll()){
            if(!cc.isAdmin(c.getGroupId()))
            m.addRow(new Object[]{c.getIdClient(),c.getName(),c.getEmail(),cc.isAdmin(c.getGroupId()) == false ? "User" : "Gérant"});
        }
        tableClients.setModel(m);
    }
    private void listerReservations(Client c){
        DefaultTableModel m = new DefaultTableModel();
        m.setColumnIdentifiers( new String[]{"ID","Date Depart","DateArrive","Nombre Personne","Nombre Nuit","Chambre Categorie","ClientName","Status"});
        for(Reservation r : rc.getByIdClient(c.getIdClient())){
            //if(rc.isOnHold(r.getIdRes()))
            m.addRow(new Object[]{r.getIdRes(),r.getDateDepart(),r.getDateArrive(),r.getNbPersonne(),r.getNbNuit(),r.getChambre().getTypeChambre(),r.getClient().getName(), sc.getById(r.getStatus())});
        }
        tableReservations.setModel(m);
    }
